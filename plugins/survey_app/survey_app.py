    def apply_striped_symbology(self, layer):
        """Apply striped symbology to a polygon layer."""
        if layer.geometryType() != QgsWkbTypes.PolygonGeometry:
            print(f"Layer {layer.name()} is not a polygon layer, skipping striped symbology")
            return

        # Create a fill symbol
        symbol = QgsFillSymbol.createSimple({})

        # Remove default layers
        for i in range(symbol.symbolLayerCount()):
            symbol.deleteSymbolLayer(0)

        # Add a solid fill as base layer
        simple_fill = QgsSimpleFillSymbolLayer()
        simple_fill.setFillColor(QColor(0, 70, 200, 100))  # Light blue with transparency
        simple_fill.setStrokeColor(QColor(0, 70, 200, 255))
        simple_fill.setStrokeWidth(0.5)
        symbol.appendSymbolLayer(simple_fill)

        # Add line pattern fill for stripes
        line_pattern_fill = QgsLinePatternFillSymbolLayer()
        line_pattern_fill.setLineAngle(45)  # 45 degree angle for stripes
        line_pattern_fill.setDistance(3.0)  # Distance between lines

        # Create a line symbol for the pattern and set its properties
        line_symbol = line_pattern_fill.subSymbol()
        if line_symbol:
            line_symbol.setWidth(1.0)  # Width of stripe lines
            line_symbol.setColor(QColor(0, 70, 200, 255))  # Stripe color

        # Add the line pattern as another symbol layer
        symbol.appendSymbolLayer(line_pattern_fill)

        # Apply the symbol to the layer
        renderer = QgsSingleSymbolRenderer(symbol)
        layer.setRenderer(renderer)
        layer.triggerRepaint()

        print(f"Applied striped symbology to layer: {layer.name()}")

    def setup_auto_xy_from_project_crs(self, layer, project_crs):
        """Configure read-only X/Y (LON/LAT) fields and auto-fill using WGS84 (EPSG:4326) coordinates.
        Assumes the layer is a point layer and uses the point geometry directly."""
        if not layer or not layer.isValid():
            print("Auto XY setup skipped: invalid layer")
            return

        # find LAT / LON (or X/Y) field indices (case-insensitive)
        fields = layer.fields()
        lat_idx = None
        lon_idx = None
        for i, f in enumerate(fields):
            n = f.name().lower()
            if n in ('lat', 'latitude', 'y'):
                lat_idx = i
            if n in ('lon', 'lng', 'long', 'longitude', 'x'):
                lon_idx = i

        if lat_idx is None or lon_idx is None:
            print("Address layer missing LAT/LON (or X/Y) fields; auto-fill disabled.")
            return

        # Set default values using expressions based on geometry transformed to WGS84
        try:
            # Get the layer's CRS auth ID
            layer_crs_authid = layer.crs().authid() if layer.crs().isValid() else 'EPSG:4326'
            target_crs = 'EPSG:4326'  # Always use WGS84 for LAT/LON
            
            # Build expressions that transform geometry to EPSG:4326 before extracting coordinates
            if layer_crs_authid != target_crs:
                # Need to transform geometry to WGS84
                lon_expression = f"x(transform($geometry, '{layer_crs_authid}', '{target_crs}'))"
                lat_expression = f"y(transform($geometry, '{layer_crs_authid}', '{target_crs}'))"
                print(f"Using transformation: {layer_crs_authid} -> {target_crs}")
            else:
                # No transformation needed, already in WGS84
                lon_expression = "x($geometry)"
                lat_expression = "y($geometry)"
                print(f"No CRS transformation needed, layer already in: {target_crs}")

            # Set default value expressions that will be evaluated when the feature is created
            layer.setDefaultValueDefinition(lon_idx, 
                QgsDefaultValue(lon_expression, True))

            layer.setDefaultValueDefinition(lat_idx, 
                QgsDefaultValue(lat_expression, True))

            print(f"Set default value expressions for LAT/LON fields using WGS84 (EPSG:4326)")
        except Exception as e:
            print(f"Could not set default value expressions: {e}")

        # Mark the fields read-only in the form
        try:
            ro_setup = QgsEditorWidgetSetup('TextEdit', {'ReadOnly': True})
            layer.setEditorWidgetSetup(lat_idx, ro_setup)
            layer.setEditorWidgetSetup(lon_idx, ro_setup)
        except Exception as e:
            print(f"Could not set editor widgets to read-only: {e}")

        print(f"Auto X/Y setup complete for layer: {layer.name()} (storing as EPSG:4326)")