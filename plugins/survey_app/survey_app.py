    def copy_layer_with_specific_fields(self, original_layer, layer_attributes, photo_fields, target_crs, clip_layer=None, layer_type=None):
        """Copy a layer with ALL original fields, plus UUID and SURVEYED field.
        All layers are transformed to the target_crs (EPSG:31370).
        If clip_layer is provided, the features will be clipped to that layer's geometry."""
        
        if not original_layer or not original_layer.isValid():
            return None

        temp_layer = None
        try:
            # Always use EPSG:31370 as target CRS
            target_crs = QgsCoordinateReferenceSystem('EPSG:31370')
            
            # Check if this layer should be empty
            should_be_empty = layer_type in ['facade', 'pole', 'aerial', 'crossing']

            if should_be_empty:
                print(f"Creating empty layer for {layer_type}")
                # Create an empty memory layer
                temp_layer = QgsVectorLayer(
                    f"{QgsWkbTypes.displayString(original_layer.wkbType())}?crs=EPSG:31370", 
                    f"temp_{original_layer.name()}", 
                    "memory"
                )

                if not temp_layer.isValid():
                    print("Failed to create empty temporary memory layer")
                    return None

                # Start editing to add fields
                temp_layer.startEditing()
                data_provider = temp_layer.dataProvider()

                # Add fields
                original_fields = original_layer.fields()
                data_provider.addAttributes(original_fields)

                # Add UUID field
                uuid_field = QgsField("UUID", QVariant.String, "string", 38)
                data_provider.addAttributes([uuid_field])

                # Add SURVEYED field
                surveyed_field = QgsField("SURVEYED", QVariant.Bool, "bool")
                data_provider.addAttributes([surveyed_field])

                # Add any missing photo fields
                for photo_field in photo_fields:
                    if temp_layer.fields().indexFromName(photo_field) == -1:
                        data_provider.addAttributes([
                            QgsField(photo_field, QVariant.String, "string", 255)
                        ])

                # Add missing required fields
                if layer_attributes:
                    existing_names = [f.name().lower() for f in temp_layer.fields()]
                    for attr in layer_attributes:
                        if attr.lower() not in existing_names:
                            data_provider.addAttributes([QgsField(attr, QVariant.String, "string", 255)])

                temp_layer.updateFields()

                # Commit changes
                if not temp_layer.commitChanges():
                    print(f"Failed to commit changes for empty {layer_type} layer")
                    return None

                print(f"Successfully created empty {layer_type} layer")
                return temp_layer

            # For non-empty layers, process normally
            # Clip if needed
            working_layer = original_layer
            if clip_layer and clip_layer.isValid():
                try:
                    from qgis import processing # type: ignore
                    print(f"Clipping {original_layer.name()} with survey area polygon...")
                    
                    # Ensure both layers are in EPSG:31370 for clipping
                    if original_layer.crs().authid() != 'EPSG:31370':
                        # Reproject original layer to EPSG:31370
                        reprojected_result = processing.run("native:reprojectlayer", {
                            'INPUT': original_layer,
                            'TARGET_CRS': QgsCoordinateReferenceSystem('EPSG:31370'),
                            'OUTPUT': 'memory:'
                        })
                        original_layer = reprojected_result['OUTPUT']
                    
                    clipped_result = processing.run("native:clip", {
                        'INPUT': original_layer,
                        'OVERLAY': clip_layer,
                        'OUTPUT': 'memory:'
                    })
                    
                    working_layer = clipped_result['OUTPUT']
                    if working_layer.isValid():
                        print(f"Clipping successful: {working_layer.featureCount()} features")
                    else:
                        print("Clipping failed, using original layer")
                        working_layer = original_layer
                        
                except Exception as clip_error:
                    print(f"Processing clip failed: {clip_error}, using original layer")
                    working_layer = original_layer

            # SPECIAL PROCESSING FOR SIDEWALK LAYER - Split long segments
            is_sidewalk_layer = (layer_type == 'sidewalk')
            if is_sidewalk_layer and working_layer.geometryType() == QgsWkbTypes.LineGeometry:
                print("Processing sidewalk layer - splitting segments longer than 30 meters")
                # Use the split_long_sidewalk_segments function
                working_layer = self.split_long_sidewalk_segments(working_layer, max_length=30.0, target_crs=target_crs)
                if working_layer:
                    print(f"After splitting: {working_layer.featureCount()} features")

            # Create temporary layer in EPSG:31370
            temp_layer = QgsVectorLayer(
                f"{QgsWkbTypes.displayString(working_layer.wkbType())}?crs=EPSG:31370", 
                f"temp_{working_layer.name()}", 
                "memory"
            )

            if not temp_layer.isValid():
                print("Failed to create temporary memory layer")
                return None

            # Start editing
            temp_layer.startEditing()
            data_provider = temp_layer.dataProvider()

            # Copy fields
            original_fields = working_layer.fields()
            data_provider.addAttributes(original_fields)

            # Add UUID field
            uuid_field = QgsField("UUID", QVariant.String, "string", 38)
            data_provider.addAttributes([uuid_field])

            # Add SURVEYED field
            surveyed_field = QgsField("SURVEYED", QVariant.Bool, "bool")
            data_provider.addAttributes([surveyed_field])

            # Add any missing photo fields
            for photo_field in photo_fields:
                if temp_layer.fields().indexFromName(photo_field) == -1:
                    data_provider.addAttributes([
                        QgsField(photo_field, QVariant.String, "string", 255)
                    ])

            # Add missing required fields
            if layer_attributes:
                existing_names = [f.name().lower() for f in temp_layer.fields()]
                for attr in layer_attributes:
                    if attr.lower() not in existing_names:
                        data_provider.addAttributes([QgsField(attr, QVariant.String, "string", 255)])

            temp_layer.updateFields()

            # Copy features with coordinate transformation if needed
            features = []
            uuid_counter = 0
            
            # Check if we need to transform coordinates
            source_crs = working_layer.crs()
            need_transform = source_crs.isValid() and source_crs != target_crs
            
            for original_feature in working_layer.getFeatures():
                geometry = original_feature.geometry()
                
                if need_transform and not geometry.isEmpty():
                    try:
                        transform = QgsCoordinateTransform(source_crs, target_crs, QgsProject.instance())
                        geometry.transform(transform)
                    except Exception as transform_error:
                        print(f"Coordinate transformation failed: {transform_error}")

                new_feature = QgsFeature(temp_layer.fields())
                new_feature.setGeometry(geometry)

                # Copy all original attributes
                for field in original_fields:
                    field_name = field.name()
                    if field_name in original_feature.fields().names():
                        new_feature.setAttribute(field_name, original_feature[field_name])

                # Set new fields
                new_feature.setAttribute("UUID", str(uuid_counter))
                new_feature.setAttribute("SURVEYED", False)
                uuid_counter += 1

                features.append(new_feature)

            # Add all features
            if features:
                data_provider.addFeatures(features)

            # Commit changes
            if not temp_layer.commitChanges():
                print(f"Failed to commit changes for layer {working_layer.name()}")
                return None

            print(f"Created temporary layer with {temp_layer.featureCount()} features")
            return temp_layer

        except Exception as e:
            print(f"Error copying layer {original_layer.name() if original_layer else 'None'}: {e}")
            import traceback
            print(traceback.format_exc())
            if temp_layer and temp_layer.isValid() and temp_layer.isEditable():
                temp_layer.rollBack()
            return None

