    def apply_surveyed_symbology(self, layer):
        """Apply rule-based symbology to distinguish surveyed vs unsurveyed features."""
        if not layer or not layer.isValid():
            return

        geom_type = layer.geometryType()

        # Define colors
        surveyed_color = '46,204,113,255'      # Green for surveyed
        unsurveyed_color = '231,76,60,255'     # Red for unsurveyed

        # Define colors for buildings
        surveyed_color_bd = '0,70,200,255'     # Blue for surveyed
        unsurveyed_color_bd = '200,200,0,255'     # Yellow for unsurveyed

        # Create root rule
        root_rule = QgsRuleBasedRenderer.Rule(None)

        # Rule for surveyed features (SURVEYED = 1)
        if geom_type == QgsWkbTypes.PolygonGeometry:
            surveyed_symbol = QgsFillSymbol.createSimple({
                'color': surveyed_color_bd,
                'outline_color': '0,70,200,255',
                'outline_width': '0.5',
                'outline_style': 'solid'
            })
            unsurveyed_symbol = QgsFillSymbol.createSimple({
                'color': unsurveyed_color_bd,
                'outline_color': '200,200,0,255',
                'outline_width': '0.5',
                'outline_style': 'solid'
            })
        elif geom_type == QgsWkbTypes.LineGeometry:
            surveyed_symbol = QgsLineSymbol.createSimple({
                'color': surveyed_color,
                'width': '1.0',
                'line_style': 'solid'
            })
            unsurveyed_symbol = QgsLineSymbol.createSimple({
                'color': unsurveyed_color,
                'width': '1.0',
                'line_style': 'solid'
            })
        elif geom_type == QgsWkbTypes.PointGeometry:
            surveyed_symbol = QgsMarkerSymbol.createSimple({
                'color': surveyed_color,
                'outline_color': '39,174,96,255',
                'size': '4',
                'name': 'circle'
            })
            unsurveyed_symbol = QgsMarkerSymbol.createSimple({
                'color': unsurveyed_color,
                'outline_color': '192,57,43,255',
                'size': '4',
                'name': 'circle'
            })
        else:
            print(f"Unknown geometry type for layer: {layer.name()}")
            return

        # Create rules
        surveyed_rule = QgsRuleBasedRenderer.Rule(surveyed_symbol)
        surveyed_rule.setFilterExpression('"SURVEYED" = 1')
        surveyed_rule.setLabel('Surveyed')

        unsurveyed_rule = QgsRuleBasedRenderer.Rule(unsurveyed_symbol)
        unsurveyed_rule.setFilterExpression('"SURVEYED" = 0 OR "SURVEYED" IS NULL')
        unsurveyed_rule.setLabel('Not Surveyed')

        # Add rules to root
        root_rule.appendChild(surveyed_rule)
        root_rule.appendChild(unsurveyed_rule)

        # Create and apply renderer
        renderer = QgsRuleBasedRenderer(root_rule)
        layer.setRenderer(renderer)
        layer.triggerRepaint()

        print(f"Applied surveyed symbology to layer: {layer.name()}")

