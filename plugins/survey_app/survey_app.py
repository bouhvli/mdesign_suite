    def split_long_sidewalk_segments(self, layer, max_length=30.0, target_crs=None):
        """Split sidewalk segments longer than max_length into smaller segments using QGIS processing.

        If target_crs is provided and differs from layer.crs(), reproject the layer to target_crs
        first so that the LENGTH parameter (meters) is applied in linear units consistent with the
        project CRS.
        """

        if not layer or layer.geometryType() != QgsWkbTypes.LineGeometry:
            print("Invalid layer for splitting")
            return layer

        try:
            from qgis import processing  # type: ignore

            print(f"Splitting sidewalk segments longer than {max_length} meters...")

            layer_to_split = layer

            # Reproject to target_crs if provided and different - split algorithm uses layer units
            if target_crs and target_crs.isValid() and layer.crs().isValid() and layer.crs() != target_crs:
                try:
                    print(f"Reprojecting sidewalk layer from {layer.crs().authid()} to {target_crs.authid()} before splitting")
                    reproj_result = processing.run("native:reprojectlayer", {
                        'INPUT': layer,
                        'TARGET_CRS': target_crs,
                        'OUTPUT': 'memory:'
                    })
                    reproj_layer = reproj_result.get('OUTPUT')
                    if reproj_layer and reproj_layer.isValid():
                        layer_to_split = reproj_layer
                        print(f"Reprojection successful: {layer_to_split.crs().authid()}")
                    else:
                        print("Reprojection failed, will attempt to split original layer")
                except Exception as reproj_err:
                    print(f"Reprojection error: {reproj_err}, continuing with original layer")

            print(f"Feature count before split: {layer_to_split.featureCount()}")

            # Use QGIS native split by length algorithm
            result = processing.run("native:splitlinesbylength", {
                'INPUT': layer_to_split,
                'LENGTH': float(max_length),
                'OUTPUT': 'memory:'
            })

            split_layer = result.get('OUTPUT')

            if not split_layer or not split_layer.isValid():
                print("Splitting returned invalid layer - returning original")
                return layer

            # Ensure multipart parts are exploded to singlepart features (some inputs produce multipart)
            try:
                multi_to_single = processing.run("native:multiparttosingleparts", {
                    'INPUT': split_layer,
                    'OUTPUT': 'memory:'
                })
                single_layer = multi_to_single.get('OUTPUT')
                if single_layer and single_layer.isValid():
                    split_layer = single_layer
            except Exception:
                # Not fatal — keep split_layer as-is
                pass

            if split_layer and split_layer.isValid():
                print(f"Split successful: {split_layer.featureCount()} features after splitting")

                # Verify splitting worked by checking lengths (in layer units)
                long_segments_count = 0
                for feature in split_layer.getFeatures():
                    geom = feature.geometry()
                    if geom is None or geom.isEmpty():
                        continue
                    length = geom.length()
                    if length > max_length * 1.01:  # small tolerance
                        long_segments_count += 1
                        print(f"Long segment found: {length:.2f} (units of layer CRS)")

                if long_segments_count > 0:
                    print(f"Warning: {long_segments_count} segments still longer than {max_length} (layer units)")
                else:
                    print("All segments are within the maximum length limit")

                return split_layer
            else:
                print("Splitting failed - returning original layer")
                return layer

        except Exception as e:
            print(f"Error in split_long_sidewalk_segments: {e}")
            import traceback
            print(traceback.format_exc())
            return layer

