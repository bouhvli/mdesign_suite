    def is_image_field(self, field_name, field_value):
        """Check if a field might contain image paths/URLs."""
        if not field_value:
            return False

        str_value = str(field_value).strip()
        if not str_value or str_value == "NULL":
            return False

        # Check field name patterns
        image_field_names = [
            "photo", "image", "picture", "img", "myphoto", "foto",
            "photograph", "photo_path", "image_path", "picture_path",
            "url", "link", "attachment", "start", "end", "before", "after",
            "left", "right", "front", "back", "side", "top", "bottom",
        ]
        if any(img_name in field_name.lower() for img_name in image_field_names):
            return True

        # Check value patterns for image extensions
        image_extensions = [
            ".jpg", ".jpeg", ".png", ".gif", ".bmp", ".tiff", ".tif", ".webp",
        ]
        if any(str_value.lower().endswith(ext) for ext in image_extensions):
            return True

        # Check for URL patterns
        if str_value.startswith(("http://", "https://", "file://", "www.")):
            return True

        # Check for common path patterns
        if "/" in str_value or "\\" in str_value:
            return True

        return False

