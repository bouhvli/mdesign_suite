class ClickableLabel(QLabel):
    """A clickable QLabel that emits a signal when clicked."""

    clicked = pyqtSignal(str, object)  # field_name, field_value

    def __init__(self, text, field_name, field_value, parent=None):
        super().__init__(text, parent)
        self.field_name = field_name
        self.field_value = field_value
        self.setStyleSheet("color: blue; text-decoration: underline; cursor: pointer;")
        self.setCursor(Qt.PointingHandCursor)
        self.setToolTip(f"Click to open image: {field_value}")

    def mousePressEvent(self, event):
        """Handle mouse click events."""
        try:
            self.clicked.emit(self.field_name, self.field_value)
            event.accept()
        except Exception:
            super().mousePressEvent(event)


